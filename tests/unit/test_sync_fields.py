@pytest.mark.unit
class TestWorkoutSessionComputedProperties:
    """Validate the exercise_count and total_volume computed properties."""

    def test_exercise_count_empty_session(self, db_session: Session):
        ws = _make_session(db_session)
        db_session.commit()
        db_session.refresh(ws)
        assert ws.exercise_count == 0

    def test_exercise_count_with_exercises(self, db_session: Session):
        ex1 = _make_exercise(db_session, "Squat")
        ex2 = _make_exercise(db_session, "Press")
        ws = _make_session(db_session)
        _make_log_exercise(db_session, ws, ex1)
        _make_log_exercise(db_session, ws, ex2)
        db_session.commit()
        db_session.refresh(ws)
        assert ws.exercise_count == 2

    def test_total_volume_empty_session(self, db_session: Session):
        ws = _make_session(db_session)
        db_session.commit()
        db_session.refresh(ws)
        assert ws.total_volume == 0.0

    def test_total_volume_with_sets(self, db_session: Session):
        ex = _make_exercise(db_session)
        ws = _make_session(db_session)
        le = _make_log_exercise(db_session, ws, ex)
        # 3 sets × 10 reps × 100 kg = 3000
        _make_log_set(db_session, le, set_number=1, reps=10, weight=100.0)
        _make_log_set(db_session, le, set_number=2, reps=10, weight=100.0)
        _make_log_set(db_session, le, set_number=3, reps=10, weight=100.0)
        db_session.commit()
        db_session.refresh(ws)
        assert ws.total_volume == 3000.0
