@pytest.mark.unit
class TestSyncFieldsInApiSchemas:
    """Validate that client_uuid + timestamps are preserved through Pydantic schemas."""

    def test_workout_session_create_accepts_client_uuid(self):
        from app.schemas.workout_schema import WorkoutSessionCreate

        payload = {
            "workout_date": "2026-01-15",
            "exercises": [],
            "client_uuid": str(uuid4()),
        }
        schema = WorkoutSessionCreate(**payload)
        assert schema.client_uuid is not None

    def test_workout_session_create_without_client_uuid(self):
        from app.schemas.workout_schema import WorkoutSessionCreate

        schema = WorkoutSessionCreate(workout_date=date.today(), exercises=[])
        assert schema.client_uuid is None  # optional — server will generate

    def test_set_detail_accepts_client_uuid(self):
        from app.schemas.workout_schema import SetDetail

        supplied = str(uuid4())
        sd = SetDetail(set_number=1, reps=10, weight=100.0, client_uuid=supplied)
        assert str(sd.client_uuid) == supplied

